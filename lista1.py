számlista = [1, 16, 28, 32, 86, 99]

print(f"Elemek: {számlista}")#kiírja az elemeket
print(f"Az első szám:{számlista[0]}")#minden lista első tajga
print(f"Az elemek száma: {len(számlista)}")#kiírja a lista elemszámát
print(f"A legnagyobb elem: {max(számlista)}")#kiírja a legnagyobb elemet
print(f"A legkisebb elem: {min(számlista)}")#kiírja a legkisebb elemet
print(f"Elemek összege: {sum(számlista)}")#összedaja az elemeket
print(f"Utolsó elem: {számlista[len(számlista)-1]}")#kiírja a mindnekori# utolsó elemet

